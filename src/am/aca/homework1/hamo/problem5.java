package am.aca.homework1.hamo;

public class problem5 {
    public static void main(String[] args) {
        int n1 = 100;
        int n2 = 10;
        System.out.println("100 * 10 =" + (n1 * n2));
        System.out.println("100 / 10 =" + (n1 / n2));
        System.out.println("100 + 10 =" + (n1 + n2));
        System.out.println("100 - 10 =" + (n1 - n2));
        System.out.println("100 % 10 =" + (n1 % n2));
    }
}
