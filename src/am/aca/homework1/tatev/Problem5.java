/*Write a Java program to print the sum (addition), multiply, subtract, divide and remainder 
of two numbers.*/
public class Problem5 {
	public static void main(String[] args) {
//		int x = 4;
//		int y = 15;
		
		double x = 4;
		double y = 15;
		
		System.out.println(x + y);
		System.out.println(x * y);
		System.out.println(x / y);
		System.out.println(x % y);
		System.out.println(x - y);
	}
}
