package am.aca.homework1.ars;

class Problem5 {
    public static void main(String[] args) {
        float number0 = 100;
        float number1 = 8;
        System.out.println("100 + 8 = " + (number0 + number1));
        System.out.println("100 - 8 = " + (number0 - number1));
        System.out.println("100 * 8 = " + (number0 * number1));
        System.out.println("100 / 8 = " + number0 / number1);
        System.out.println("100 % 8 = " + (number0 % number1));
    }
}